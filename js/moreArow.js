// no parameter arrow function
const pie=()=>3.1416;
// one parameter arrow function
const double=(number)=>number*2;
// simplified version of one parameter arrow function(bracket deya lge na r ki)
const triple=number=>number*3;
// simple arrow function
const sum=(a,b,c,d,e)=>a+b+c+d+e;
// multiline arrow function(multiline ar khetre return krte hy...onno gulay return kra lge na....)
const doMath=(a,b,c)=>{
    const sum=a+b+c;
    const mul=a*b*c;
    const div=mul/sum;
    return div;
}